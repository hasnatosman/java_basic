//Given x find cube of x.(Hint Math.pow)

class exercise3{
	public static void main(String args[]){
		
		// square of x
		double x = Math.pow(25, 2);
		
		// square root of y
		double y = Math.sqrt(25);
		
		// cube of z
		double z = Math.pow(3,3);
		
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
	}
}